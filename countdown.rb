def countdown(nums)
  while nums > 0
    puts "#{nums} SECOND(S)!"
    nums -=1









  end

  return "HAPPY NEW YEAR!"

end

def countdown_with_sleep(nums)
  while nums > 0
    puts "#{nums} SECOND(S)!"
    nums -=1
    sleep(1)
  end












  return "HAPPY NEW YEAR!"

end
